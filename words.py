import random

# Список простых слов для угадывания
WORDS = [
    'кот', 'дом', 'лес', 'нос', 'рот', 'год', 'день', 'ночь',
    'стол', 'стул', 'окно', 'дверь', 'река', 'море', 'город',
    'яблоко', 'солнце', 'медведь', 'машина', 'компьютер'
]

def get_random_word():
    """Возвращает случайное слово из списка"""
    return random.choice(WORDS)

